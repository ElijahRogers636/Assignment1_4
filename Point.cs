using System;

namespace Assignment1_4
{

    public class Point
    {
        public double x { get; set; } 
        public double y { get; set; }

        public Point(double xCoord, double yCoord)
        {
            x = xCoord;
            y = yCoord;
        }

        public Point()
        {
            x = 0; x = 0;
        }

    }
}
